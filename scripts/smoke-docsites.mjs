import { mkdtempSync, mkdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs'
import { tmpdir } from 'node:os'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { execFileSync } from 'node:child_process'

const repo = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const tmp = mkdtempSync(join(tmpdir(), 'doc-bridge-docsites-'))
const version = JSON.parse(readFileSync(join(repo, 'package.json'), 'utf8')).version
const tarball = join(repo, `agentskit-doc-bridge-${version}.tgz`)

const run = (cmd, args, cwd = repo) =>
  execFileSync(cmd, args, { cwd, encoding: 'utf8', stdio: ['ignore', 'pipe', 'pipe'] })

const write = (path, value) => {
  mkdirSync(dirname(path), { recursive: true })
  writeFileSync(path, value)
}

const install = () => {
  rmSync(tarball, { force: true })
  run('npm', ['run', 'build'])
  run('npm', ['pack', '--json'])
  run('npm', ['init', '-y'], tmp)
  run('npm', ['install', tarball], tmp)
  return join(tmp, 'node_modules', '.bin', 'ak-docs')
}

const baseRepo = (root) => {
  write(join(root, 'pnpm-workspace.yaml'), "packages:\n  - 'packages/*'\n")
  write(join(root, 'packages/auth/package.json'), '{"name":"@acme/auth"}\n')
  write(join(root, 'docs/for-agents/INDEX.md'), '# Agent docs\n\nStart here.\n')
  write(join(root, 'docs/for-agents/packages/auth.md'), '# Auth package\n\nAuth ownership.\n')
}

const smokeFumadocs = (bin) => {
  const root = join(tmp, 'fumadocs')
  baseRepo(root)
  write(join(root, 'doc-bridge.config.json'), JSON.stringify({
    schemaVersion: 1,
    corpus: {
      agent: { root: 'docs/for-agents' },
      human: { plugin: 'fumadocs', options: { contentDir: 'apps/web/content/docs', urlPrefix: '/docs' } },
    },
    routing: {
      plugin: 'pnpm-monorepo',
      options: {
        packages: ['packages/*'],
        ownership: { auth: { path: 'packages/auth', agentDoc: 'docs/for-agents/packages/auth.md' } },
      },
    },
    gates: { preset: 'standard' },
  }, null, 2))
  write(join(root, 'apps/web/content/docs/meta.json'), '{"pages":["index","(reference)"]}\n')
  write(join(root, 'apps/web/content/docs/index.mdx'), '---\nid: auth\n---\n# Auth overview\n')
  write(join(root, 'apps/web/content/docs/hidden.mdx'), '---\nid: hidden\n---\n# Hidden\n')
  write(join(root, 'apps/web/content/docs/(reference)/meta.json'), '{"pages":["api"]}\n')
  write(join(root, 'apps/web/content/docs/(reference)/api/index.mdx'), '---\nid: api\n---\n# API reference\n')

  const bootstrap = JSON.parse(run(bin, ['bootstrap', 'agent-docs'], root))
  const created = bootstrap.created.join('\n')
  if (!created.includes('docs/for-agents/human/auth.md')) throw new Error('Fumadocs missing auth draft')
  if (!created.includes('docs/for-agents/human/api.md')) throw new Error('Fumadocs missing grouped api draft')
  if (created.includes('hidden')) throw new Error('Fumadocs included hidden meta page')
  run(bin, ['index'], root)
  run(bin, ['gate', 'run', 'human-guide-links'], root)
  const index = JSON.parse(run('node', ['-e', "process.stdout.write(JSON.stringify(require('./.doc-bridge/index.json')))"], root))
  if (index.lookup.ownership.auth.humanDoc !== '/docs') throw new Error(`bad Fumadocs humanDoc ${index.lookup.ownership.auth.humanDoc}`)
}

const smokeDocusaurus = (bin) => {
  const root = join(tmp, 'docusaurus')
  baseRepo(root)
  write(join(root, 'doc-bridge.config.json'), JSON.stringify({
    schemaVersion: 1,
    corpus: {
      agent: { root: 'docs/for-agents' },
      human: {
        plugin: 'docusaurus',
        options: { docsDir: 'website/docs', sidebarsFile: 'website/sidebars.ts', urlPrefix: '/docs' },
      },
    },
    routing: {
      plugin: 'pnpm-monorepo',
      options: {
        packages: ['packages/*'],
        ownership: { auth: { path: 'packages/auth', agentDoc: 'docs/for-agents/packages/auth.md' } },
      },
    },
    gates: { preset: 'standard' },
  }, null, 2))
  write(join(root, 'website/sidebars.ts'), [
    "import type { SidebarsConfig } from '@docusaurus/plugin-content-docs'",
    '',
    'const sidebars: SidebarsConfig = {',
    "  tutorialSidebar: [{ type: 'doc', id: 'guide/part1' }, { type: 'autogenerated', dirName: 'api' }],",
    '}',
    '',
    'export default sidebars',
    '',
  ].join('\n'))
  write(join(root, 'website/docs/guide/hello.md'), '---\nid: part1\npackage: auth\nslug: /hello-world\n---\n# Hello\n')
  write(join(root, 'website/docs/guide/hidden.md'), '# Hidden\n')
  write(join(root, 'website/docs/api/index.md'), '# API\n')

  const bootstrap = JSON.parse(run(bin, ['bootstrap', 'agent-docs'], root))
  const created = bootstrap.created.join('\n')
  if (!created.includes('docs/for-agents/human/auth.md')) throw new Error('Docusaurus missing package-joined auth draft')
  if (!created.includes('docs/for-agents/human/api.md')) throw new Error('Docusaurus missing autogenerated api draft')
  if (created.includes('hidden')) throw new Error('Docusaurus included hidden sidebar page')
  run(bin, ['index'], root)
  run(bin, ['gate', 'run', 'human-guide-links'], root)
  const index = JSON.parse(run('node', ['-e', "process.stdout.write(JSON.stringify(require('./.doc-bridge/index.json')))"], root))
  if (index.lookup.ownership.auth.humanDoc !== '/docs/hello-world') throw new Error(`bad Docusaurus humanDoc ${index.lookup.ownership.auth.humanDoc}`)
}

try {
  const bin = install()
  smokeFumadocs(bin)
  smokeDocusaurus(bin)
  process.stdout.write(`docsite smoke passed: ${tmp}\n`)
} finally {
  rmSync(tarball, { force: true })
}
